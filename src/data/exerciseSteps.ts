export const STEP_DURATION_MS = 15_000;

const PROGRAM_INDEX_KEY = "bugun-bir-cicek-program-index";

export function getNextExerciseProgram(): readonly string[] {
  let index = 0;
  try {
    const savedIndex = Number.parseInt(localStorage.getItem(PROGRAM_INDEX_KEY) ?? "0", 10);
    if (Number.isInteger(savedIndex) && savedIndex >= 0) index = savedIndex % exercisePrograms.length;
    localStorage.setItem(PROGRAM_INDEX_KEY, String((index + 1) % exercisePrograms.length));
  } catch (error) {
    console.warn("Yönlendirme sırası kaydedilemedi; ilk paket kullanılıyor.", error);
  }
  return exercisePrograms[index];
}
