const QUOTE_INDEX_KEY = "dilaranin-bahcesi-completion-quote-index";

export function getNextCompletionQuote(): CompletionQuote {
  let index = 0;

  try {
    const savedIndex = Number.parseInt(localStorage.getItem(QUOTE_INDEX_KEY) ?? "0", 10);
    if (Number.isFinite(savedIndex) && savedIndex >= 0) index = savedIndex % completionQuotes.length;
    localStorage.setItem(QUOTE_INDEX_KEY, String((index + 1) % completionQuotes.length));
  } catch (error) {
    console.warn("Söz sırası bu cihazda kaydedilemedi.", error);
  }

  return completionQuotes[index];
}
